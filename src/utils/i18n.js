class I18n {
    static getLanguage(guildSettings, userPrefs, interaction = null) {
        if (userPrefs && userPrefs.language) {
            return userPrefs.language;
        }
        if (interaction && interaction.locale) {
            const loc = interaction.locale.toLowerCase();
            if (loc.startsWith('es')) return 'es';
            if (loc.startsWith('fr')) return 'fr';
            if (loc.startsWith('de')) return 'de';
            if (loc.startsWith('pt')) return 'pt';
            if (loc.startsWith('it')) return 'it';
            if (loc.startsWith('ru')) return 'ru';
            if (loc.startsWith('hi')) return 'hi';
            if (loc.startsWith('ja')) return 'ja';
            if (loc.startsWith('ko')) return 'ko';
            if (loc.startsWith('zh')) return 'zh';
            if (loc.startsWith('en')) return 'en';
        }
        if (guildSettings && guildSettings.language) {
            return guildSettings.language;
        }
        return 'en';
    }

    static t(lang, key, replacements = {}) {
        const langPack = translations[lang] || translations['en'];
        let template = langPack[key] || translations['en'][key] || key;

        Object.keys(replacements).forEach(k => {
            template = template.replace(new RegExp(`{${k}}`, 'g'), replacements[k]);
        });

        return template;
    }
}

module.exports = I18n;
